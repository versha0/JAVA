
 class Student{
    private int studentid;
    private String name; 
    private int age;
    
    public void setstudentid(int id){
        this.studentid=id;
    }
    public int getstudentid(){
        return studentid;
    }
    public void setname(String name){
        this.name=name;
    }
    public String getname(){
        return name;
    }
    public void setage(int id){
        this.age=id;
    }
    public int getage(){
        return age;
    }
    

}
 